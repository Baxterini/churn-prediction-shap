    "\"\"\"\n",
    "train.py - Bank Churn baseline model (produkcyjny pipeline)\n",
    "\n",
    "Co robimy:\n",
    "1) Wczytujemy dane\n",
    "2) Usuwamy kolumny techniczne/ID (RowNumber, CustomerId, Surname)\n",
    "3) Dzielimy na train/test (stratyfikacja po target)\n",
    "4) Budujemy preprocessing (OneHot + skalowanie)\n",
    "5) Trenujemy model (Logistic Regression baseline)\n",
    "6) Liczymy metryki (ROC-AUC, F1, confusion matrix, classification report)\n",
    "7) Zapisujemy pipeline do pliku (joblib) -> gotowe do użycia w Streamlit\n",
    "\"\"\"\n",
    "\n",
    "from __future__ import annotations\n",
    "\n",
    "import os\n",
    "from pathlib import Path\n",
    "\n",
    "import joblib\n",
    "import numpy as np\n",
    "import pandas as pd\n",
    "\n",
    "from sklearn.compose import ColumnTransformer\n",
    "from sklearn.linear_model import LogisticRegression\n",
    "from sklearn.metrics import (\n",
    "    roc_auc_score,\n",
    "    f1_score,\n",
    "    confusion_matrix,\n",
    "    classification_report,\n",
    ")\n",
    "from sklearn.model_selection import train_test_split\n",
    "from sklearn.pipeline import Pipeline\n",
    "from sklearn.preprocessing import OneHotEncoder, StandardScaler\n",
    "from sklearn.impute import SimpleImputer\n",
    "\n",
    "\n",
    "# ---------- Konfiguracja ścieżek ----------\n",
    "DATA_PATH = Path(\"data/churn.csv\")\n",
    "MODEL_DIR = Path(\"models\")\n",
    "MODEL_DIR.mkdir(exist_ok=True)\n",
    "MODEL_PATH = MODEL_DIR / \"churn_pipeline.joblib\"\n",
    "\n",
    "\n",
    "def load_data(path: Path) -> pd.DataFrame:\n",
    "    \"\"\"Wczytuje dane z CSV i robi podstawową walidację.\"\"\"\n",
    "    if not path.exists():\n",
    "        raise FileNotFoundError(f\"Nie znaleziono pliku: {path.resolve()}\")\n",
    "\n",
    "    df = pd.read_csv(path)\n",
    "\n",
    "    # Minimalna walidacja: czy mamy target?\n",
    "    if \"Exited\" not in df.columns:\n",
    "        raise ValueError(\"Brakuje kolumny targetu 'Exited' w danych.\")\n",
    "\n",
    "    return df\n",
    "\n",
    "\n",
    "def prepare_features(df: pd.DataFrame) -> tuple[pd.DataFrame, pd.Series]:\n",
    "    \"\"\"\n",
    "    Przygotowuje X i y:\n",
    "    - usuwa kolumny techniczne / identyfikatory\n",
    "    - zwraca macierz cech X i target y\n",
    "    \"\"\"\n",
    "    # Te kolumny nie opisują zachowania klienta (artefakt/ID/dane osobowe)\n",
    "    drop_cols = [\"RowNumber\", \"CustomerId\", \"Surname\"]\n",
    "\n",
    "    # errors='ignore' chroni nas, gdyby dataset miał trochę inne nazwy\n",
    "    X = df.drop(columns=drop_cols + [\"Exited\"], errors=\"ignore\")\n",
    "    y = df[\"Exited\"].astype(int)\n",
    "\n",
    "    return X, y\n",
    "\n",
    "\n",
    "def build_pipeline(X: pd.DataFrame) -> Pipeline:\n",
    "    \"\"\"\n",
    "    Buduje pipeline:\n",
    "    - num: imputacja + skalowanie\n",
    "    - cat: imputacja + OneHot\n",
    "    - model: LogisticRegression (baseline)\n",
    "    \"\"\"\n",
    "    # Wykrywanie typów kolumn (prosto i skutecznie)\n",
    "    numeric_features = X.select_dtypes(include=[\"int64\", \"float64\"]).columns.tolist()\n",
    "    categorical_features = X.select_dtypes(include=[\"object\", \"category\", \"bool\"]).columns.tolist()\n",
    "\n",
    "    # Pipeline dla danych numerycznych\n",
    "    numeric_transformer = Pipeline(\n",
    "        steps=[\n",
    "            # jeśli są braki -> wstaw medianę (bezpieczna strategia)\n",
    "            (\"imputer\", SimpleImputer(strategy=\"median\")),\n",
    "            # skalowanie jest ważne dla logreg (i wielu modeli liniowych)\n",
    "            (\"scaler\", StandardScaler()),\n",
    "        ]\n",
    "    )\n",
    "\n",
    "    # Pipeline dla danych kategorycznych\n",
    "    categorical_transformer = Pipeline(\n",
    "        steps=[\n",
    "            (\"imputer\", SimpleImputer(strategy=\"most_frequent\")),\n",
    "            # handle_unknown=\"ignore\" pozwala działać na nowych kategoriach w produkcji\n",
    "            (\"onehot\", OneHotEncoder(handle_unknown=\"ignore\")),\n",
    "        ]\n",
    "    )\n",
    "\n",
    "    # ColumnTransformer: łączy obróbkę num + cat\n",
    "    preprocessor = ColumnTransformer(\n",
    "        transformers=[\n",
    "            (\"num\", numeric_transformer, numeric_features),\n",
    "            (\"cat\", categorical_transformer, categorical_features),\n",
    "        ],\n",
    "        remainder=\"drop\",\n",
    "    )\n",
    "\n",
    "    # Model baseline\n",
    "    model = LogisticRegression(\n",
    "        max_iter=2000,\n",
    "        class_weight=\"balanced\",  # pomaga przy lekkim niezbalansowaniu 80/20\n",
    "        n_jobs=None,\n",
    "        random_state=42,\n",
    "    )\n",
    "\n",
    "    # Finalny pipeline: preprocessing + model\n",
    "    pipe = Pipeline(steps=[(\"preprocess\", preprocessor), (\"model\", model)])\n",
    "\n",
    "    return pipe\n",
    "\n",
    "\n",
    "def evaluate_model(pipe: Pipeline, X_test: pd.DataFrame, y_test: pd.Series) -> None:\n",
    "    \"\"\"Liczy metryki i ładnie je wypisuje.\"\"\"\n",
    "    # Prawdopodobieństwa klasy 1 (churn=1) -> do ROC-AUC\n",
    "    y_proba = pipe.predict_proba(X_test)[:, 1]\n",
    "\n",
    "    # Predykcje 0/1 przy progu 0.5 (baseline). Później możemy stroić threshold.\n",
    "    y_pred = (y_proba >= 0.5).astype(int)\n",
    "\n",
    "    roc = roc_auc_score(y_test, y_proba)\n",
    "    f1 = f1_score(y_test, y_pred)\n",
    "    cm = confusion_matrix(y_test, y_pred)\n",
    "\n",
    "    print(\"\\n=== Wyniki (test) ===\")\n",
    "    print(f\"ROC-AUC: {roc:.4f}\")\n",
    "    print(f\"F1:      {f1:.4f}\")\n",
    "    print(\"\\nConfusion matrix:\")\n",
    "    print(cm)\n",
    "\n",
    "    print(\"\\nClassification report:\")\n",
    "    print(classification_report(y_test, y_pred, digits=4))\n",
    "\n",
    "\n",
    "def main() -> None:\n",
    "    # 1) Load\n",
    "    df = load_data(DATA_PATH)\n",
    "\n",
    "    # 2) X, y\n",
    "    X, y = prepare_features(df)\n",
    "\n",
    "    # 3) Split (stratify -> zachowujemy proporcje klas w train/test)\n",
    "    X_train, X_test, y_train, y_test = train_test_split(\n",
    "        X,\n",
    "        y,\n",
    "        test_size=0.2,\n",
    "        random_state=42,\n",
    "        stratify=y,\n",
    "    )\n",
    "\n",
    "    # 4) Pipeline\n",
    "    pipe = build_pipeline(X_train)\n",
    "\n",
    "    # 5) Train\n",
    "    pipe.fit(X_train, y_train)\n",
    "\n",
    "    # 6) Eval\n",
    "    evaluate_model(pipe, X_test, y_test)\n",
    "\n",
    "    # 7) Save\n",
    "    joblib.dump(pipe, MODEL_PATH)\n",
    "    print(f\"\\n✅ Zapisano model: {MODEL_PATH.resolve()}\")\n",
    "\n",
    "\n",
    "if __name__ == \"__main__\":\n",
    "    main()\n"
